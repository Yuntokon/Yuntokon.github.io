var tags = [];
function readTags()
{
	tags = [];
	var all = {};
	var group = [];
	var groupName = "null";

	for (let tag of allTags)
	{
		let norm = tag.toLowerCase();

		if (norm.startsWith("#"))
		{
			group = [];
			groupName = tag.substring(1);
			tags[groupName] = group;
		}
		else if (norm)
		{
			group.push(norm);
			if (all[norm])
				console.log('Duplicate tag: ' + norm + " (" + all[norm] + " " + groupName + ")");
			else
				all[norm] = groupName;
		}
	}
}
readTags();


function randomTag(type)
{
	return tags[type][Math.floor(Math.random() * tags[type].length)]
}

register({
	title: "Sketch",
	description: "Free sketching",
	category: "_",
	sideLayout: false,
	drawMode: 2,
	
	settings:
	{
		drawingPrompt: true,
		framingPrompt: false,
		expressionPrompt: true,
		hairPrompt: true,
		clothesPrompt: true,
		extraPrompt: false,
		actionPrompt: false,
		locationPrompt: false,
		minRatio: 0.5,
		maxRatio: 1.5,
		gridIntersecs: 0,
		padding: 0.1,
	},
	
	onNext: function()
	{
		prompt = [];
		if (settings.framingPrompt)
		{
			prompt.push(randomTag("frame"));
			prompt.push(randomTag("angle"));
		}

		if (settings.expressionPrompt)
			prompt.push(randomTag("expression"));

		if (settings.hairPrompt)
		{
			prompt.push(randomTag("hair"));
			if (Math.random() < 0.2)
				prompt.push(randomTag("hair extra"));
		}
		
		if (settings.extraPrompt)
		{
			if (Math.random() < 0.3)
				prompt.push(randomTag("flower"));
			else if (Math.random() < 0.1)
				prompt.push(randomTag("extra"));
			else
				prompt.push(randomTag("fashion"));
		}
		
		if (settings.clothesPrompt)
		{
			prompt.push(randomTag("dress"));
			if (Math.random() < 0.2)
				prompt.push(randomTag("sleeves"));
			if (Math.random() < 0.1)
				prompt.push(randomTag("gloves"));
			if (Math.random() < 0.5)
				prompt.push(randomTag("dress bottom"));
			if (Math.random() < 0.2)
				prompt.push(randomTag("legwear"));
			if (Math.random() < 0.5)
				prompt.push(randomTag("shoes"));
		}
		
		prompt.push(randomTag("accessory"));

		if (settings.actionPrompt)
		{
			if (Math.random() < 0.6)
				prompt.push(randomTag("gesture"));

			else if (Math.random() < 0.6)
				prompt.push(randomTag("pose"));
			else
				prompt.push(randomTag("action"));
		}
		
		if (settings.locationPrompt)
			prompt.push(randomTag("location"));

		return {
			ratio: settings.minRatio + Math.random() * (settings.maxRatio - settings.minRatio),
			prompt: prompt.join(" | ")
		}
	},

	onQuestion: function()
	{
		clear(ctx1);
		
		var ratio = question.ratio;
		var l, r, b, t, w, h;
		if (ratio < canvasRatio)
		{
			h = canvasTop - canvasBottom; w = ratio * h; l = w * -0.5; b = canvasBottom;
		}
		else
		{
			w = canvasRight - canvasLeft; h = w / ratio; l = canvasLeft; b = h * -0.5;
		}
		
		r = w + l;
		t = h + b;
		
		setCanvasRect(canvasLeft, canvasRight, canvasBottom, canvasTop, window.innerWidth < window.innerHeight, h);

		l *= 1 - settings.padding;
		r *= 1 - settings.padding;
		t *= 1 - settings.padding;
		b *= 1 - settings.padding;
		w *= 1 - settings.padding;
		h *= 1 - settings.padding;
		
		ctx1.fillStyle = "#FFFFFFFF";
		ctx1.filter = "drop-shadow(0px 2px 8px " + colorShadow + ")";
		ctx1.fillRect(l, b, w, h);
		ctx1.filter = "none";
				
		if (settings.gridIntersecs > -1)
		{
			ctx1.lineWidth = lineWidth(2);
			ctx1.strokeStyle = colorBold;

			ctx1.beginPath();
			ctx1.moveTo(l, t);
			ctx1.lineTo(r, t);
			ctx1.lineTo(r, b);
			ctx1.lineTo(l, b);
			ctx1.lineTo(l, t);
			ctx1.stroke();

			ctx1.globalCompositeOperation = "difference";
			ctx1.lineWidth = lineWidth(1);
			ctx1.strokeStyle = "#FFFFFFA0";
			ctx1.beginPath();
			var n = settings.gridIntersecs + 1
			for (var i = 1; i < n; ++i)
			{
				ctx1.moveTo(l + w * i / n, b);
				ctx1.lineTo(l + w * i / n, t);
				
				ctx1.moveTo(l, b + h * i / n);
				ctx1.lineTo(r, b + h * i / n);
			}
			ctx1.stroke();	
			ctx1.globalCompositeOperation = "normal";
		}
		
		if (settings.drawingPrompt)
			drawText(ctx1, question.prompt, 0, b - 0.02, 4, colorBold);
		
		clear(ctx2);
		redrawDrawmode(true);			
	},

	onAnswered: function()
	{		
		updateAverages({});
		next();
	},

	onDraw: function()
	{		

	}
});